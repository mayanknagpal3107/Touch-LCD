#ifndef Delay_Class
#define Delay_Class 1

#include <util/delay.h>

namespace Delay{

	////////////////////////////////////////////    DELAY ROUTINE    /////////////////////////////////////////
	void wait_ms(long ms){
		long r;
		r = ms/10;
		while(r>0)
		{
			_delay_ms(10);
			r--;
		}
	}

	////////////////////////////////////////////    DELAY ROUTINE    /////////////////////////////////////////
	void wait_us(long us){
		long r;
		r = us/10;
		while(r>0)
		{
			_delay_us(10);
			r--;
		}
	}
}
#endif